// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let output = [];
  if(step === undefined){
    step = 1;
  }

  if(start === end){
    return [];
  }

  if(step > 0){
    for(let i = start; i <= end; i += step){
      output.push(i)

    }

  }else{
    for(let i = start; i >= end; i += step){
      output.push(i)

    }
  }
  return output;
}

